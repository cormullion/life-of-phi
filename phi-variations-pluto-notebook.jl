md"The Greek letter Phi is used throughout Greece, maths, physics, and computing. 

There are about 20 different versions of the phi character in the Unicode tables, but the three main versions used in computing are:

```
- 03D5: œï 
- 03C6: œÜ 
- 0278: …∏ 
```

These designs are from the JuliaMono font. 

Here are the same glyphs shown in Pluto's default text font (Alegreya Sans, unless modified by the browser).

- 03D5: œï 
- 03C6: œÜ 
- 0278: …∏ 

Remember that font fallbacks are used in browsers: ie, if the required font doesn't have a glyph for a character, then other fonts are searched until a suitable glyph is found. So because Alegreya doesn't have anything for U+0278, the browser shows something similar. (On my system I get U+03C6 from Helvetica here!)

"

